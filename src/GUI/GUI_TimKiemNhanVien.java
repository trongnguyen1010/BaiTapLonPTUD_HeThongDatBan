    public void loadData() {
        List<NhanVien> listNhanVien = NV_DAO.getAllNhanVien();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        int stt = 1;
        for (NhanVien nv : listNhanVien) {
            model.addRow(new Object[]{
                stt++,
                nv.getMaNhanVien(),
                nv.getTen(),
                nv.getSdt(),
                nv.getEmail(),
                nv.getGioiTinh(),
                nv.getChucVu()
            });
        }
    }

