    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {                                             
        String searchText = jTextField1.getText().trim();
        
        // Kiểm tra nếu là chữ "Tìm kiếm" thì coi như không có từ khóa tìm kiếm
        if (searchText.equals("Tìm kiếm")) {
            searchText = "";
        }
        
        String searchType = (String) jComboBox1.getSelectedItem();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        List<NhanVien> nhanViens = NV_DAO.searchNhanVien(searchType, searchText);
        int stt = 1;
        
        for (NhanVien nv : nhanViens) {
            model.addRow(new Object[]{
                stt++,
                nv.getMaNhanVien(),
                nv.getTen(),
                nv.getSdt(),
                nv.getEmail(),
                nv.getGioiTinh(),
                nv.getChucVu()
            });
        }
    }                                             

